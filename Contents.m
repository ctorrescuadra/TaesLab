% Read Data Models
%   ReadDataModel           - Reads a data model file.
%   CopyDataModel           - Copies a data model file to another format.
%
% Get Thermoeconomic Results
%   ThermoeconomicModel     - Gets the complete thermoeconomic model of a plant.
%   ProductiveStructure     - Shows information about the productive structure of a plant.
%   ProductiveDiagram       - Gets the adjacency tables of the productive structure.
%   ExergyAnalysis          - Exergy analysis for one state of the plant
%   ThermoeconomicAnalysis  - Thermoeconomic analysis of one state of the plant
%   ThermoeconomicDiagnosis - Compares one state of the plant with its reference state.
%   WasteAnalysis           - Waste recycling analysis of one state of the plant. 
%   DiagramFP               - Gets the FP diagram of one state of the plant 
%   SummaryResults          - Gets the summary results of a thermo-economic model.
%
% Save Results Tables 
%   SaveResults             - Saves the tables of a cResultSet object to a file.
%   SaveSummary             - Saves the result summary tables to a file.
%   SaveDataModel           - Saves the data model tables to a file.
%   SaveTable               - Saves a table to a file
%
% Display Results 
%   ListResultTables        - Lists the results tables and their properties.
%   ShowResults             - Displays the model's results tables.
%   ShowTable               - Displays an individual table.
%   ShowGraph               - Displays the graph associated to a results table.
%   ExportResults           - Exports the results tables in different formats.
%
% GUI functions
%   TaesPanel               - Select parameters for the thermo-economic model.
%   ResultsPanel            - Interactive results viewer.
%   ViewResults             - Displays the model results in a GUI application.
%